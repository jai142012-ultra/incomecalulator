// ============================================================
// Professional PDF Generator for Visualsier
// Uses jsPDF + jsPDF-AutoTable for beautiful, branded PDF documents
//
// Dependencies (must be loaded before this file):
//   - jsPDF
//   - jsPDF-AutoTable
//
// Exposes global functions:
//   - addPDFHeader(doc, title, subtitle)
//   - addPDFFooter(doc, pageNum, totalPages)
//   - addSummaryCard(doc, x, y, width, label, value, color)
//   - formatCurrencyPDF(amount)
//   - generateProfessionalAccountPDF(account, transactions, balance, totalEarnings, totalExpenses)
//   - generateProfessionalWishlistPDF(account, wishlist, balance)
//   - generateProfessionalPurchaseReceiptPDF(item, account, balanceBefore, balanceAfter, transactions, remainingWishlist)
// ============================================================

// ----------------------------------------------------------
// Color palette — consistent branding across all PDF exports
// ----------------------------------------------------------
const PDF_COLORS = {
    primary: [99, 102, 241],      // Indigo
    secondary: [6, 182, 212],     // Cyan
    success: [16, 185, 129],      // Emerald
    danger: [244, 63, 94],        // Rose
    warning: [245, 158, 11],      // Amber
    dark: [6, 9, 19],             // Background
    textDark: [30, 41, 59],       // Slate 800
    textMuted: [100, 116, 139],   // Slate 500
    white: [255, 255, 255],
    lightGray: [241, 245, 249],   // Slate 100
    medGray: [226, 232, 240],     // Slate 200
};

// ----------------------------------------------------------
// addPDFHeader
// Renders a branded header with gradient bar, brand name,
// tagline, date/time, document title, and optional subtitle.
// Returns the Y position immediately below the header area.
// ----------------------------------------------------------
function addPDFHeader(doc, title, subtitle) {
    const pageW = doc.internal.pageSize.getWidth();

    // Top gradient bar
    doc.setFillColor(...PDF_COLORS.primary);
    doc.rect(0, 0, pageW, 28, 'F');

    // Secondary accent stripe
    doc.setFillColor(...PDF_COLORS.secondary);
    doc.rect(0, 28, pageW, 3, 'F');

    // Brand name
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(18);
    doc.setTextColor(...PDF_COLORS.white);
    doc.text('VISUALSIER', 14, 12);

    // Tagline
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(8);
    doc.setTextColor(200, 210, 230);
    doc.text('Smart Job Tracker & Financial Planner', 14, 19);

    // Date on right
    doc.setFontSize(9);
    doc.setTextColor(...PDF_COLORS.white);
    const dateStr = new Date().toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' });
    doc.text(dateStr, pageW - 14, 12, { align: 'right' });

    // Time
    doc.setFontSize(8);
    doc.text(new Date().toLocaleTimeString('en-IN', { hour: '2-digit', minute: '2-digit' }), pageW - 14, 19, { align: 'right' });

    // Document title below header bar
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(16);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text(title, 14, 44);

    // Subtitle (optional)
    if (subtitle) {
        doc.setFont('helvetica', 'normal');
        doc.setFontSize(10);
        doc.setTextColor(...PDF_COLORS.textMuted);
        doc.text(subtitle, 14, 52);
    }

    return 58; // Y position after header
}

// ----------------------------------------------------------
// addPDFFooter
// Renders a branded footer with a divider line, generation
// attribution text, page numbers, and an accent bar.
// ----------------------------------------------------------
function addPDFFooter(doc, pageNum, totalPages) {
    const pageW = doc.internal.pageSize.getWidth();
    const pageH = doc.internal.pageSize.getHeight();

    // Footer line
    doc.setDrawColor(...PDF_COLORS.medGray);
    doc.setLineWidth(0.5);
    doc.line(14, pageH - 18, pageW - 14, pageH - 18);

    // Footer text
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(7);
    doc.setTextColor(...PDF_COLORS.textMuted);
    doc.text('Generated by Visualsier — Smart Job Tracker & Financial Planner', 14, pageH - 12);
    doc.text(`Page ${pageNum} of ${totalPages}`, pageW - 14, pageH - 12, { align: 'right' });

    // Tiny brand accent at bottom
    doc.setFillColor(...PDF_COLORS.primary);
    doc.rect(0, pageH - 4, pageW, 4, 'F');
}

// ----------------------------------------------------------
// addSummaryCard
// Renders a rounded summary card with a colored left accent
// bar, a muted label, and a bold value. Useful for KPI rows.
// ----------------------------------------------------------
function addSummaryCard(doc, x, y, width, label, value, color) {
    // Card background
    doc.setFillColor(...PDF_COLORS.lightGray);
    doc.roundedRect(x, y, width, 22, 3, 3, 'F');

    // Left color accent bar
    doc.setFillColor(...color);
    doc.rect(x, y, 3, 22, 'F');

    // Label
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(8);
    doc.setTextColor(...PDF_COLORS.textMuted);
    doc.text(label, x + 8, y + 8);

    // Value
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(13);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text(value, x + 8, y + 18);
}

// ----------------------------------------------------------
// formatCurrencyPDF
// Formats a numeric amount into Indian Rupee (₹) notation
// with two decimal places and locale-aware grouping.
// ----------------------------------------------------------
function formatCurrencyPDF(amount) {
    return '₹' + parseFloat(amount).toLocaleString('en-IN', {
        minimumFractionDigits: 2,
        maximumFractionDigits: 2
    });
}


// ==========================================
// ACCOUNT DETAILS PDF
// ==========================================
// Generates a complete account statement with:
//   - Summary cards (balance, earnings, expenses)
//   - Account information table
//   - Full transaction history (sorted newest-first)
//   - Net balance footer
//   - Branded header/footer on every page
// ==========================================
function generateProfessionalAccountPDF(account, transactions, balance, totalEarnings, totalExpenses) {
    const { jsPDF } = window.jspdf;
    const doc = new jsPDF('p', 'mm', 'a4');
    const pageW = doc.internal.pageSize.getWidth();

    // --- PAGE 1: Account Overview ---
    let y = addPDFHeader(doc, 'Account Statement', `Account: ${account.name}`);

    // Summary cards row
    const cardW = (pageW - 42) / 3;
    addSummaryCard(doc, 14, y, cardW, 'Current Balance', formatCurrencyPDF(balance), PDF_COLORS.secondary);
    addSummaryCard(doc, 14 + cardW + 7, y, cardW, 'Total Earnings', formatCurrencyPDF(totalEarnings), PDF_COLORS.success);
    addSummaryCard(doc, 14 + (cardW + 7) * 2, y, cardW, 'Total Expenses', formatCurrencyPDF(totalExpenses), PDF_COLORS.danger);
    y += 30;

    // Account info table
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(11);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text('Account Information', 14, y);
    y += 4;

    doc.autoTable({
        startY: y,
        head: [['Property', 'Value']],
        body: [
            ['Account Name', account.name],
            ['Starting Balance', formatCurrencyPDF(account.initialBalance || 0)],
            ['Current Balance', formatCurrencyPDF(balance)],
            ['Total Transactions', String(transactions.length)],
            ['Statement Date', new Date().toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' })]
        ],
        theme: 'grid',
        headStyles: { fillColor: PDF_COLORS.primary, textColor: PDF_COLORS.white, fontSize: 9, fontStyle: 'bold' },
        bodyStyles: { fontSize: 9, textColor: PDF_COLORS.textDark },
        alternateRowStyles: { fillColor: PDF_COLORS.lightGray },
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 50 } },
        margin: { left: 14, right: 14 }
    });
    y = doc.lastAutoTable.finalY + 10;

    // Transaction history table
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(11);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text('Transaction History', 14, y);
    y += 4;

    // Sort transactions newest-first
    const txSorted = [...transactions].sort((a, b) => new Date(b.date) - new Date(a.date));
    const txBody = txSorted.map(tx => {
        const d = new Date(tx.date);
        const dateStr = isNaN(d.getTime()) ? tx.date : d.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' });
        const timeStr = isNaN(d.getTime()) ? '' : d.toLocaleTimeString('en-IN', { hour: '2-digit', minute: '2-digit' });
        return [
            dateStr + '\n' + timeStr,
            tx.name + (tx.notes ? '\n' + tx.notes : ''),
            tx.type === 'earning' ? 'Earning' : 'Expense',
            (tx.type === 'earning' ? '+' : '-') + formatCurrencyPDF(tx.amount)
        ];
    });

    if (txBody.length === 0) {
        txBody.push(['—', 'No transactions recorded', '—', '—']);
    }

    doc.autoTable({
        startY: y,
        head: [['Date & Time', 'Description', 'Type', 'Amount']],
        body: txBody,
        theme: 'striped',
        headStyles: { fillColor: PDF_COLORS.primary, textColor: PDF_COLORS.white, fontSize: 9, fontStyle: 'bold' },
        bodyStyles: { fontSize: 8, textColor: PDF_COLORS.textDark, cellPadding: 3 },
        alternateRowStyles: { fillColor: [248, 250, 252] },
        columnStyles: {
            0: { cellWidth: 35 },
            2: { cellWidth: 22, halign: 'center' },
            3: { cellWidth: 32, halign: 'right', fontStyle: 'bold' }
        },
        didParseCell: function(data) {
            // Color-code the Amount column
            if (data.section === 'body' && data.column.index === 3) {
                const val = data.cell.raw;
                if (val.startsWith('+')) {
                    data.cell.styles.textColor = PDF_COLORS.success;
                } else if (val.startsWith('-')) {
                    data.cell.styles.textColor = PDF_COLORS.danger;
                }
            }
            // Color-code the Type column
            if (data.section === 'body' && data.column.index === 2) {
                const val = data.cell.raw;
                if (val === 'Earning') {
                    data.cell.styles.textColor = PDF_COLORS.success;
                } else {
                    data.cell.styles.textColor = PDF_COLORS.danger;
                }
            }
        },
        margin: { left: 14, right: 14 }
    });

    // Final balance summary box
    const finalY = doc.lastAutoTable.finalY + 8;
    doc.setFillColor(...PDF_COLORS.lightGray);
    doc.roundedRect(pageW - 90, finalY, 76, 16, 2, 2, 'F');
    doc.setFillColor(...PDF_COLORS.primary);
    doc.rect(pageW - 90, finalY, 3, 16, 'F');
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(8);
    doc.setTextColor(...PDF_COLORS.textMuted);
    doc.text('Net Account Balance', pageW - 82, finalY + 6);
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(12);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text(formatCurrencyPDF(balance), pageW - 82, finalY + 13);

    // Add footer to all pages
    const totalPages = doc.internal.getNumberOfPages();
    for (let i = 1; i <= totalPages; i++) {
        doc.setPage(i);
        addPDFFooter(doc, i, totalPages);
    }

    doc.save(`Visualsier_Account_${account.name.replace(/\s+/g, '_')}_${new Date().toISOString().slice(0, 10)}.pdf`);
}


// ==========================================
// WISHLIST REPORT PDF
// ==========================================
// Generates a wishlist status report with:
//   - Summary cards (balance, total wish cost, affordable count)
//   - Wishlist items table with affordability status
//   - Financial summary (surplus / shortfall)
//   - Branded header/footer on every page
// ==========================================
function generateProfessionalWishlistPDF(account, wishlist, balance) {
    const { jsPDF } = window.jspdf;
    const doc = new jsPDF('p', 'mm', 'a4');
    const pageW = doc.internal.pageSize.getWidth();

    let y = addPDFHeader(doc, 'Wishlist Status Report', `Account: ${account.name}`);

    const totalWishCost = wishlist.reduce((sum, w) => sum + w.cost, 0);
    const affordableCount = wishlist.filter(w => balance >= w.cost).length;

    // Summary cards
    const cardW = (pageW - 42) / 3;
    addSummaryCard(doc, 14, y, cardW, 'Account Balance', formatCurrencyPDF(balance), PDF_COLORS.secondary);
    addSummaryCard(doc, 14 + cardW + 7, y, cardW, 'Total Wish Cost', formatCurrencyPDF(totalWishCost), PDF_COLORS.warning);
    addSummaryCard(doc, 14 + (cardW + 7) * 2, y, cardW, 'Affordable Items', `${affordableCount} of ${wishlist.length}`, PDF_COLORS.success);
    y += 30;

    // Wishlist items table
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(11);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text('Wishlist Items', 14, y);
    y += 4;

    const wishBody = wishlist.map((w, idx) => {
        const canAfford = balance >= w.cost;
        const shortfall = canAfford ? '—' : formatCurrencyPDF(w.cost - balance);
        return [
            String(idx + 1),
            w.name,
            formatCurrencyPDF(w.cost),
            canAfford ? '✓ Affordable' : '✗ Need More',
            shortfall
        ];
    });

    if (wishBody.length === 0) {
        wishBody.push(['—', 'No items in wishlist', '—', '—', '—']);
    }

    doc.autoTable({
        startY: y,
        head: [['#', 'Item Name', 'Cost (₹)', 'Status', 'Shortfall']],
        body: wishBody,
        theme: 'striped',
        headStyles: { fillColor: PDF_COLORS.warning, textColor: PDF_COLORS.white, fontSize: 9, fontStyle: 'bold' },
        bodyStyles: { fontSize: 9, textColor: PDF_COLORS.textDark, cellPadding: 3.5 },
        alternateRowStyles: { fillColor: [255, 251, 235] },
        columnStyles: {
            0: { cellWidth: 12, halign: 'center' },
            2: { cellWidth: 32, halign: 'right', fontStyle: 'bold' },
            3: { cellWidth: 30, halign: 'center' },
            4: { cellWidth: 28, halign: 'right' }
        },
        didParseCell: function(data) {
            // Color-code Status column
            if (data.section === 'body' && data.column.index === 3) {
                const val = data.cell.raw;
                if (val.includes('Affordable')) {
                    data.cell.styles.textColor = PDF_COLORS.success;
                    data.cell.styles.fontStyle = 'bold';
                } else {
                    data.cell.styles.textColor = PDF_COLORS.danger;
                    data.cell.styles.fontStyle = 'bold';
                }
            }
            // Color-code Shortfall column
            if (data.section === 'body' && data.column.index === 4) {
                if (data.cell.raw !== '—') {
                    data.cell.styles.textColor = PDF_COLORS.danger;
                }
            }
        },
        margin: { left: 14, right: 14 }
    });

    // Financial summary section
    const summY = doc.lastAutoTable.finalY + 10;
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(11);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text('Financial Summary', 14, summY);

    const surplus = balance - totalWishCost;
    doc.autoTable({
        startY: summY + 4,
        body: [
            ['Total Wishlist Cost', formatCurrencyPDF(totalWishCost)],
            ['Current Account Balance', formatCurrencyPDF(balance)],
            [surplus >= 0 ? 'Surplus After All Purchases' : 'Shortfall to Buy All', formatCurrencyPDF(Math.abs(surplus))]
        ],
        theme: 'grid',
        bodyStyles: { fontSize: 10, textColor: PDF_COLORS.textDark },
        columnStyles: {
            0: { fontStyle: 'bold', cellWidth: 80 },
            1: { halign: 'right', fontStyle: 'bold' }
        },
        didParseCell: function(data) {
            // Highlight final surplus/shortfall row
            if (data.section === 'body' && data.row.index === 2 && data.column.index === 1) {
                data.cell.styles.textColor = surplus >= 0 ? PDF_COLORS.success : PDF_COLORS.danger;
                data.cell.styles.fontSize = 12;
            }
        },
        margin: { left: 14, right: 14 }
    });

    // Footer on all pages
    const totalPages = doc.internal.getNumberOfPages();
    for (let i = 1; i <= totalPages; i++) {
        doc.setPage(i);
        addPDFFooter(doc, i, totalPages);
    }

    doc.save(`Visualsier_Wishlist_${account.name.replace(/\s+/g, '_')}_${new Date().toISOString().slice(0, 10)}.pdf`);
}


// ==========================================
// PURCHASE RECEIPT PDF (2-page)
// ==========================================
// Generates a two-page purchase receipt:
//   PAGE 1 — Account Balance:
//     - Balance before/after cards
//     - Purchase details table
//     - Recent transaction history (last 8)
//   PAGE 2 — Wishlist Status:
//     - Purchased item highlight banner
//     - Remaining wishlist items with affordability
//   Both pages include branded header/footer.
// ==========================================
function generateProfessionalPurchaseReceiptPDF(item, account, balanceBefore, balanceAfter, transactions, remainingWishlist) {
    const { jsPDF } = window.jspdf;
    const doc = new jsPDF('p', 'mm', 'a4');
    const pageW = doc.internal.pageSize.getWidth();

    // --- PAGE 1: Account Balance ---
    let y = addPDFHeader(doc, 'Purchase Receipt — Account Balance', `Account: ${account.name}`);

    // Balance summary cards
    const cardW = (pageW - 35) / 2;
    addSummaryCard(doc, 14, y, cardW, 'Balance Before Purchase', formatCurrencyPDF(balanceBefore), PDF_COLORS.secondary);
    addSummaryCard(doc, 14 + cardW + 7, y, cardW, 'Balance After Purchase', formatCurrencyPDF(balanceAfter), PDF_COLORS.success);
    y += 30;

    // Purchase details
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(11);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text('Purchase Details', 14, y);
    y += 4;

    doc.autoTable({
        startY: y,
        body: [
            ['Item Purchased', item.name],
            ['Amount Debited', formatCurrencyPDF(item.cost)],
            ['Balance Before', formatCurrencyPDF(balanceBefore)],
            ['Balance After', formatCurrencyPDF(balanceAfter)],
            ['Transaction Date', new Date().toLocaleString('en-IN')],
            ['Authorization', 'Password Verified ✓']
        ],
        theme: 'grid',
        bodyStyles: { fontSize: 9, textColor: PDF_COLORS.textDark },
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 50 } },
        didParseCell: function(data) {
            // Highlight Amount Debited in danger color
            if (data.section === 'body' && data.row.index === 1 && data.column.index === 1) {
                data.cell.styles.textColor = PDF_COLORS.danger;
                data.cell.styles.fontStyle = 'bold';
            }
        },
        margin: { left: 14, right: 14 }
    });
    y = doc.lastAutoTable.finalY + 10;

    // Recent transactions
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(11);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text('Recent Transaction History', 14, y);
    y += 4;

    const recentTxs = [...transactions].sort((a, b) => new Date(b.date) - new Date(a.date)).slice(0, 8);
    const txBody = recentTxs.map(tx => {
        const d = new Date(tx.date);
        const dateStr = isNaN(d.getTime()) ? tx.date : d.toLocaleDateString('en-IN', { day: '2-digit', month: 'short' });
        return [
            dateStr,
            tx.name,
            (tx.type === 'earning' ? '+' : '-') + formatCurrencyPDF(tx.amount)
        ];
    });

    doc.autoTable({
        startY: y,
        head: [['Date', 'Description', 'Amount']],
        body: txBody.length > 0 ? txBody : [['—', 'No records', '—']],
        theme: 'striped',
        headStyles: { fillColor: PDF_COLORS.primary, textColor: PDF_COLORS.white, fontSize: 9 },
        bodyStyles: { fontSize: 8, textColor: PDF_COLORS.textDark },
        alternateRowStyles: { fillColor: [248, 250, 252] },
        columnStyles: { 2: { halign: 'right', fontStyle: 'bold' } },
        didParseCell: function(data) {
            // Color-code Amount column
            if (data.section === 'body' && data.column.index === 2) {
                const v = data.cell.raw;
                data.cell.styles.textColor = v.startsWith('+') ? PDF_COLORS.success : PDF_COLORS.danger;
            }
        },
        margin: { left: 14, right: 14 }
    });

    addPDFFooter(doc, 1, 2);

    // --- PAGE 2: Wishlist Status ---
    doc.addPage();
    y = addPDFHeader(doc, 'Purchase Receipt — Wishlist Status', 'Remaining wishlist items after purchase');

    // Purchased item highlight banner
    doc.setFillColor(254, 242, 242);
    doc.roundedRect(14, y, pageW - 28, 16, 2, 2, 'F');
    doc.setFillColor(...PDF_COLORS.danger);
    doc.rect(14, y, 3, 16, 'F');
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(9);
    doc.setTextColor(...PDF_COLORS.danger);
    doc.text('PURCHASED:', 22, y + 6);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text(`${item.name}  —  ${formatCurrencyPDF(item.cost)}`, 50, y + 6);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(8);
    doc.setTextColor(...PDF_COLORS.textMuted);
    doc.text(`Remaining Balance: ${formatCurrencyPDF(balanceAfter)}`, 22, y + 13);
    y += 24;

    // Remaining wishlist table
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(11);
    doc.setTextColor(...PDF_COLORS.textDark);
    doc.text('Remaining Wishlist Items', 14, y);
    y += 4;

    const wishBody = remainingWishlist.map((w, idx) => {
        const canAfford = balanceAfter >= w.cost;
        return [
            String(idx + 1),
            w.name,
            formatCurrencyPDF(w.cost),
            canAfford ? '✓ Affordable' : '✗ Need More'
        ];
    });

    if (wishBody.length === 0) {
        wishBody.push(['—', '🎉 All wishlist items have been purchased!', '—', '—']);
    }

    doc.autoTable({
        startY: y,
        head: [['#', 'Item Name', 'Cost', 'Status']],
        body: wishBody,
        theme: 'striped',
        headStyles: { fillColor: PDF_COLORS.warning, textColor: PDF_COLORS.white, fontSize: 9 },
        bodyStyles: { fontSize: 9, textColor: PDF_COLORS.textDark },
        alternateRowStyles: { fillColor: [255, 251, 235] },
        columnStyles: {
            0: { cellWidth: 12, halign: 'center' },
            2: { cellWidth: 30, halign: 'right', fontStyle: 'bold' },
            3: { cellWidth: 30, halign: 'center' }
        },
        didParseCell: function(data) {
            // Color-code Status column
            if (data.section === 'body' && data.column.index === 3) {
                data.cell.styles.textColor = data.cell.raw.includes('Affordable') ? PDF_COLORS.success : PDF_COLORS.danger;
                data.cell.styles.fontStyle = 'bold';
            }
        },
        margin: { left: 14, right: 14 }
    });

    addPDFFooter(doc, 2, 2);

    doc.save(`Visualsier_Purchase_${item.name.replace(/\s+/g, '_')}_${new Date().toISOString().slice(0, 10)}.pdf`);
}
